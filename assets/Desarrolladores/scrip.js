const verElena = document.getElementById('verElena');
const perfilElena = document.getElementById('perfilElena');
const cerrarElena = document.getElementById('cerrarElena')
const contElena = document.getElementById('contElena')


verElena.addEventListener('click', ()=>{
    perfilElena.classList.add('show')
    contElena.classList.add('traerAlFrente')
})
cerrarElena.addEventListener('click', ()=>{
    perfilElena.classList.remove('show')
    contElena.classList.remove('traerAlFrente')
})

